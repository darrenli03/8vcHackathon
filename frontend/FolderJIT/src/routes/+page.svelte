<script lang="ts">
  import { open } from "@tauri-apps/plugin-dialog";

  async function selectFile() {
    const selected = await open({
      multiple: false,
      directory: true,
    });

    if (selected === null) {
      console.log("No file selected");
    } else {
      console.log("Selected file:", selected);
    }
  }
</script>

